course = 'Anusha'
print (course.upper()) #it doesnt modify original string but creates a new string and returns it.
print (course.lower())
print(course)
print (course.find('A')) #it is case sensitive
print (course.replace('u', 'x'))
print (course.title()) #change the initial character to uppercase
print('h'in course) # it produces boolean value
#in produces boolean value while find operator returns the index of character or string.
